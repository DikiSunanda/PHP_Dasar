<?php
// Pengulangan
// For
for( $i = 0; $i <= 5; $i++) {
    echo "Hello World! <br>";
}


// while
$i = 1;
while ( $i < 5 ){
    echo "Hello World! <br>";
    $i++;
}

//do.. while
$i = 10;
do {
    echo "Hello World! <br>";
    $i++;
}while ($i < 10);

// foreach : pengulangan khusus array




?>